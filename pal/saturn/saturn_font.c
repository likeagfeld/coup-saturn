/*============================================================================
 * Font Conversion
 *============================================================================*/

void saturn_font_convert_row(uint8_t src_row, uint8_t* dst, uint8_t fg_index)
{
    if (!dst) return;

    /* Convert 8 pixels (1bpp) to 4 bytes (4bpp).
     * Each output byte holds 2 pixels as nibbles (hi=left, lo=right).
     * MSB of src is the leftmost pixel. */
    for (int i = 0; i < 4; i++) {
        int bit_hi = 7 - (i * 2);      /* Left pixel of pair */
        int bit_lo = 7 - (i * 2 + 1);  /* Right pixel of pair */

        uint8_t px_hi = (src_row >> bit_hi) & 1;
        uint8_t px_lo = (src_row >> bit_lo) & 1;

        uint8_t nibble_hi = px_hi ? fg_index : 0;
        uint8_t nibble_lo = px_lo ? fg_index : 0;

        dst[i] = (nibble_hi << 4) | nibble_lo;
    }
}

int saturn_font_convert(const uint8_t* src_1bpp, uint8_t* dst_4bpp,
                        int char_count, uint8_t fg_index)
{
    if (!src_1bpp || !dst_4bpp || char_count <= 0) return 0;

    for (int c = 0; c < char_count; c++) {
        for (int row = 0; row < SATURN_FONT_CHAR_HEIGHT; row++) {
            uint8_t src_byte = src_1bpp[c * SATURN_FONT_CHAR_HEIGHT + row];
            uint8_t* dst_ptr = &dst_4bpp[c * SATURN_FONT_4BPP_CHAR_SIZE + row * 4];
            saturn_font_convert_row(src_byte, dst_ptr, fg_index);
        }
    }

    return char_count;
}

const uint8_t* saturn_font_get_builtin(void)
{
    return (const uint8_t*)s_builtin_font_8x8;
}

int saturn_font_get_builtin_size(void)
{
    return SATURN_FONT_CHAR_COUNT * SATURN_FONT_CHAR_HEIGHT;
}

/*============================================================================
 * Multi-Font Registry
 *============================================================================*/

void saturn_font_builtin_desc(saturn_font_desc_t* desc)
{
    if (!desc) return;

    desc->name = "builtin_8x8";
    desc->data_1bpp = (const uint8_t*)s_builtin_font_8x8;
    desc->cell_width = 8;
    desc->cell_height = 8;
    desc->advance_x = 8;
    desc->first_char = SATURN_FONT_FIRST_CHAR;
    desc->char_count = SATURN_FONT_CHAR_COUNT;
    desc->bytes_per_row_1bpp = 1;  /* 8 / 8 */
}

void saturn_font_registry_init(saturn_font_registry_t* reg)
{
    if (!reg) return;

    memset(reg, 0, sizeof(saturn_font_registry_t));
    reg->count = 0;
    reg->active = -1;
    reg->vram_cursor = 0;
}

int saturn_font_register(saturn_font_registry_t* reg, const saturn_font_desc_t* desc)
{
    if (!reg || !desc) return -1;
    if (reg->count >= SATURN_FONT_MAX_FONTS) return -1;

    /* Validate descriptor */
    if (desc->cell_width == 0 || desc->cell_height == 0) return -1;
    if (desc->cell_width % 8 != 0) return -1;
    if (desc->char_count == 0) return -1;
    if (desc->cell_width > SATURN_FONT_MAX_CELL) return -1;
    if (desc->cell_height > SATURN_FONT_MAX_CELL) return -1;

    int idx = reg->count;
    saturn_font_entry_t* entry = &reg->fonts[idx];

    /* Copy descriptor */
    entry->desc = *desc;

    /* Compute 4bpp sizes */
    entry->char_4bpp_size = ((uint32_t)desc->cell_width * (uint32_t)desc->cell_height) / 2;
    entry->total_4bpp_size = (uint32_t)desc->char_count * entry->char_4bpp_size;

    /* Align VRAM cursor to 8 bytes */
    uint32_t aligned_cursor = (reg->vram_cursor + 7) & ~(uint32_t)7;
    entry->vram_offset = aligned_cursor;

    /* Advance cursor past this font's data */
    reg->vram_cursor = aligned_cursor + entry->total_4bpp_size;

    reg->count++;

    /* Auto-activate first registered font */
    if (reg->count == 1) {
        reg->active = 0;
    }

    return idx;
}

void saturn_font_set_active(saturn_font_registry_t* reg, int index)
{
    if (!reg) return;
    if (index < 0 || index >= reg->count) return;

    reg->active = index;
}

const saturn_font_entry_t* saturn_font_get_active_entry(const saturn_font_registry_t* reg)
{
    if (!reg) return NULL;
    if (reg->active < 0 || reg->active >= reg->count) return NULL;

    return &reg->fonts[reg->active];
}

void saturn_font_convert_row_wide(const uint8_t* src_row, uint8_t* dst,
                                   int bytes_per_row, uint8_t fg_index)
{
    if (!src_row || !dst || bytes_per_row <= 0) return;

    for (int i = 0; i < bytes_per_row; i++) {
        saturn_font_convert_row(src_row[i], &dst[i * 4], fg_index);
    }
}
